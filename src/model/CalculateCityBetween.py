def euclidean_distance(point1, point2):
    return ((point1[0] - point2[0]) ** 2 + (point1[1] - point2[1]) ** 2) ** 0.5

def astar(matrix, start, end):
    rows, cols = len(matrix), len(matrix[0])
    visited = [[False for _ in range(cols)] for _ in range(rows)]
    heap = [(0, start, [])]  # Priority queue (cost, current, path)

    while heap:
        cost, current, path = heappop(heap)
        row, col = current

        if current == end:
            return path + [current]

        if not (0 <= row < rows and 0 <= col < cols) or matrix[row][col] == 1 or visited[row][col]:
            continue

        visited[row][col] = True

        for dr, dc in [(1, 0), (-1, 0), (0, 1), (0, -1)]:
            new_row, new_col = row + dr, col + dc
            new_cost = cost + 1  # Assuming uniform cost for each move

            if not (0 <= new_row < rows and 0 <= new_col < cols) or matrix[new_row][new_col] == 1 or visited[new_row][new_col]:
                continue

            heuristic = euclidean_distance((new_row, new_col), end)
            total_cost = new_cost + heuristic
            heappush(heap, (total_cost, (new_row, new_col), path + [current]))

    return []

def TwoPointCity(mapa,pos1, pos2):
    # Set of values with repetitions
    values_set = set()

    path = astar(matrix, pos1, pos2)
    for it in path:
        values_set.add(abs(mapa[it[0]][it[1]]))
    # Convert the set to a list to remove repetitions
    values_set.discard(abs(mapa[pos1[0]][pos1[1]]))
    values_set.discard(abs(mapa[pos2[0]][pos2[1]]))
    unique_list = list(values_set)
    return unique_list

def calculateCityBetween(citymap,mapa):
    while bool(citymap):
        pos = next(iter(citymap.items()))
        citymap.pop(pos[0])
        for it in citymap:
            citybetween[pos[0]+it] = TwoPointCity(mapa,pos[1],citymap[it])
            citybetween[it+pos[0]] = citybetween[pos[0]+it]
    return citybetween

